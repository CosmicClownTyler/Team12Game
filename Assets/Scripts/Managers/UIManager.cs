using UnityEngine;

public class UIManager : MonoBehaviour
{
    // Singleton
    public static UIManager Instance;

    private void Awake()
    {
        // Manage singleton
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}